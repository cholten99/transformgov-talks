  <h3>Code</h3>
  <p>
    We (the organisers of TransformGov Talks) believe that our online spaces, just like our physical spaces, 
    should be free from harassment, bullying and hate.
  </p>
  <p>
    We are dedicated to providing a harassment-free experience for everyone, regardless of gender, 
    gender identity and expression, sexual orientation, disability, neurotype, physical appearance, age, race, 
    ethnicity, nationality, socio-economic status, language, religion or any other characteristic.
  </p>
  <p>
    Everyone who participates at TransformGov Talks has a responsibility to help us create safe online and 
    physical spaces and must agree to our code of conduct. This includes all attendees, organisers, volunteers 
    and sponsors.
  </p>

  <p>Unacceptable behaviour includes (but is not limited to):</p>
  <ul>
    <li>Offensive verbal comments</li>
    <li>Deliberate intimidation, bullying, harassment</li>
    <li>Stalking and / or following</li>
    <li>Violence and / or threats of violence</li>
    <li>Unwelcome sexual attention or physical contact</li>
    <li>Sharing content or links to content which is illegal, pornographic or other inappropriate material</li>
    <li>Sustained disruption of talks / sessions</li>
    <li>Other behaviour deemed inappropriate, offensive, threatening or harmful</li>
    <li>Unsolicited and / or persistent messaging in private channels (e.g. Bluesky direct messages)</li>
  </ul>

  <p>
    Instigating or encouraging any of the behaviours set out above is also unacceptable.
  </p>
  <p>
    If a participant engages in behaviour that violates this code of conduct, the organisers will take action they deem appropriate, 
    including (but not limited to) removing the participant from TransformGov Talks and banning them from future events.
  </p>

  <h3>Photography and Recording</h3>
  <p>
    When taking photographs of attendees at events please ensure you gain permission in advance. Recordings of events will be provided 
    so please do not record the event yourself.
  </p>

